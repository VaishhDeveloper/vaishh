/*program for fizz and buzz in java*/
public class Sample
{
public static void main(String args[])
{
int no;
for(int i=0;i<no;i++)
{
if(i%3==0 && i%5==0)
{
System.out.println("Fizz Buzz");
}
else if(i%5==0)
{
System.out.println("fizz");
}
else if(i%3==0)
{
System.out.println("Buzz");
}
else
{
System.out.println(" ");
}
}
}
}
